#!/usr/bin/env bash
# Build the standalone beagle compiler (no Racket dependency).
# Creates self-host/dist/compiler.cjs from compiled self-hosted modules.
set -euo pipefail

BEAGLE_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
DIST="$BEAGLE_ROOT/self-host/dist"
cd "$BEAGLE_ROOT"

echo "=== Building self-hosted modules ==="
bin/beagle-build self-host/reader.bjs 2>/dev/null
bin/beagle-build self-host/parse.bjs 2>/dev/null
bin/beagle-build self-host/check.bjs 2>/dev/null
for target in js clj py nix rkt; do
    bin/beagle-build "self-host/emit-${target}.bjs" 2>/dev/null
    cp "runtime/src/self/host/emit/${target}.js" "$DIST/emit-${target}.mjs"
done

echo "=== Creating module variants ==="

strip_tests() {
    sed '/^const passes = \[\];$/,$d'
}

strip_main() {
    sed '/^function main() {$/,$d'
}

strip_import() {
    grep -v "^import \* as .* from 'beagle/core.js';"
}

for mod in reader parse check types macros ast; do
    SRC="$BEAGLE_ROOT/runtime/src/self/host/${mod}.js"
    if [ -f "$SRC" ]; then
        strip_import < "$SRC" | strip_tests > "$DIST/${mod}.mod.mjs"
    fi
done

for target in js clj py nix rkt; do
    SRC="$DIST/emit-${target}.mjs"
    if [ -f "$SRC" ]; then
        strip_import < "$SRC" | strip_main > "$DIST/emit-${target}.mod.mjs"
    fi
done

echo "=== Bundling compiler.cjs ==="

{
    cat <<'HEADER'
// beagle-bun compiler — standalone, no Racket dependency.
// Generated by self-host/bundle-compiler.sh
const { readFileSync } = require('fs');

HEADER

    # Core runtime helpers (strip export keywords, make available globally)
    echo "// --- beagle/core.js runtime ---"
    sed 's/^export //' "$DIST/beagle/core.js"
    echo ""
    echo "const \$\$bc = { range, remove, mapcat, every_p, keep, map_indexed,"
    echo "  assoc_in, update_in, select_keys, merge_with, take_while, drop_while,"
    echo "  memoize, fnil, some_fn, every_pred, rename_keys, map_keys, map_vals,"
    echo "  disj, reduce_kv, dedupe, interpose, partition_all, partition_by,"
    echo "  split_with, zipmap, format, hash, get_in, take_nth, keep_indexed,"
    echo "  reductions, replace, max_key, min_key };"
    echo ""

    # Each module wrapped in IIFE to isolate scope (avoids duplicate const errors)
    echo "// --- reader ---"
    echo "const __reader = (function() {"
    cat "$DIST/reader.mod.mjs"
    echo "  return { read_all, read_datum };"
    echo "})();"
    echo ""

    echo "// --- parser ---"
    echo "const __parser = (function() {"
    echo "  const rereadDatum = __reader.read_datum;"
    cat "$DIST/parse.mod.mjs"
    echo "  return { parse_program };"
    echo "})();"
    echo ""

    echo "// --- checker ---"
    echo "const __checker = (function() {"
    cat "$DIST/check.mod.mjs"
    echo "  return { type_check };"
    echo "})();"
    echo ""

    for target in js clj py nix rkt; do
        MOD="$DIST/emit-${target}.mod.mjs"
        if [ -f "$MOD" ]; then
            echo "// --- emitter: ${target} ---"
            echo "const __emit_${target} = (function() {"
            cat "$MOD"
            echo "  return { emit_program };"
            echo "})();"
            echo ""
        fi
    done

    echo "// --- stdlib types ---"
    echo "const STDLIB_TYPES = $(cat "$DIST/stdlib-types.json");"
    echo ""

    cat <<'DRIVER'
// --- compiler driver ---

const EMITTERS = {
  js:  __emit_js,
  clj: __emit_clj,
  py:  __emit_py,
  nix: __emit_nix,
  rkt: __emit_rkt,
};

function main() {
  const args = process.argv.slice(2);
  let outputAst = false;
  let skipCheck = false;
  const files = [];

  for (const arg of args) {
    if (arg === '--ast') { outputAst = true; }
    else if (arg === '--no-check') { skipCheck = true; }
    else { files.push(arg); }
  }

  if (files.length === 0) {
    process.stderr.write('usage: beagle-bun <source> [--ast] [--no-check]\n');
    process.exit(2);
  }

  const file = files[0];
  const source = readFileSync(file, 'utf-8');

  // 1. Read
  const readResult = __reader.read_all(source);
  const target = readResult.target;
  const datums = readResult.datums;

  if (!target) {
    process.stderr.write('beagle-bun: no #lang target found in ' + file + '\n');
    process.exit(1);
  }

  // Inject define-target if not already present
  const hasDefineTarget = datums.some(d =>
    Array.isArray(d) && d.length >= 2 && d[0] === 'define-target');
  if (!hasDefineTarget) {
    datums.unshift(['define-target', target]);
  }

  // 2. Parse
  const prog = __parser.parse_program(datums);

  if (outputAst) {
    process.stdout.write(JSON.stringify(prog, null, 2) + '\n');
    return;
  }

  // 3. Type check (strict mode only)
  if (!skipCheck && prog.mode !== 'dynamic') {
    const result = __checker.type_check(prog);
    if (result.diagnostics) {
      for (const d of result.diagnostics) {
        process.stderr.write('beagle: ' + d.message + '\n');
      }
    }
  }

  // 4. Emit
  const emitter = EMITTERS[target];
  if (!emitter) {
    process.stderr.write('beagle-bun: unsupported target: ' + target + '\n');
    process.exit(1);
  }

  const output = emitter.emit_program(prog);
  process.stdout.write(output);
}

main();
DRIVER
} > "$DIST/compiler.cjs"

echo "=== Done: $DIST/compiler.cjs ($(wc -c < "$DIST/compiler.cjs") bytes) ==="
